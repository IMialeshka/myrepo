<template>
  <v-row justify="center">
    <v-dialog
        v-model="dialog"
        persistent
        max-width="450px"
    >
      <template v-slot:activator="{ on, attrs }">
        <v-btn
            color="primary"
            dark
            v-bind="attrs"
            v-on="on"
            v-show="$store.getters.ident"
        >
          Load book
        </v-btn>
      </template>
      <v-card>
        <v-card-title>
          <span class="text-h5">Book load</span>
        </v-card-title>
        <v-card-text>
          <v-container>
            <v-row>
              <v-col
                  cols="12"
                  v-if="this.save_dialog"
              >
              <v-img
                  lazy-src="file:/D:/BookShop/Covers/00ac891a-d55d-4a33-8bc7-7da0bd460eb1.png"
                  max-height="150"
                  max-width="250"
                  src="file:/D:/BookShop/Covers/00ac891a-d55d-4a33-8bc7-7da0bd460eb1.png"
              ></v-img>
              </v-col>
              <v-col
                  cols="12"
                  v-if="!this.save_dialog"
              >
                <v-file-input
                    truncate-length="15"
                    label="Chose file"
                    v-model="file"
                ></v-file-input>
              </v-col>
              <v-col
                  cols="12"
                  v-if="this.save_dialog"
              >
                <v-text-field
                    label="Name book"
                    v-model="name"
                ></v-text-field>
              </v-col>
                <v-col
                    cols="12"
                    v-if="this.save_dialog"
                >
                  <v-text-field
                      label="Writer"
                      v-model="writer"
                  ></v-text-field>
                </v-col>
                  <v-col
                      cols="12"
                      v-if="this.save_dialog"
                  >
                    <v-text-field
                        label="Genre"
                        v-model="genre"
                    ></v-text-field>
                  </v-col>
            </v-row>
          </v-container>
        </v-card-text>
        <v-card-actions>
          <v-spacer></v-spacer>
          <v-btn
              color="blue darken-1"
              text
              @click="parseBook"
              v-show="!this.save_dialog"
          >
            Ok
          </v-btn>
          <v-btn
              color="blue darken-1"
              text
              @click="save"
              v-show="this.save_dialog"
          >
            Save
          </v-btn>
          <v-btn
              color="blue darken-1"
              text
              @click="exit"
          >
            Exit
          </v-btn>
        </v-card-actions>
        {{message}}
      </v-card>
    </v-dialog>
  </v-row>
</template>
<script>
import axios from "axios";
export default {
  data: () => ({
    dialog: false,
    file: null,
    message: null,
    name: null,
    writer: null,
    bookFile: null,
    file_cover: null,
    path_file_cover: null,
    genre: null,
    save_dialog: false
  }),
  methods: {
    parseBook: function() {
      let bodyFormData = new FormData();
      bodyFormData.append("file", this.file);
      axios({method: "post",
        url: "/getmetainfo",
        data: bodyFormData,
        headers: { "Content-Type": "multipart/form-data" ,
                   "Authorization": "Bearer " + localStorage.getItem("jwtToken")}})
          .then(response =>
          {this.message = response.data.ErrInfo;
            if(this.message == null){
               this.name = response.data.name;
               this.writer = response.data.writer;
               this.bookFile = response.data.bookFile;
               this.file_cover = response.data.file_cover;
               this.path_file_cover = response.data.path_file_cover;
               this.genre = null;
               this.file = null;
               this.save_dialog = true;
            }
          })
          .catch(error => {
            console.log(error);
          });
    },
    save: function (){

    },
    exit: function (){
      this.save_dialog = false;
      this.name = null;
      this.writer = null;
      this.bookFile = null;
      this.file_cover = null;
      this.path_file_cover = null;
      this.file = null;
      this.genre = null;
      this.dialog = false;
    }
  }
}
</script>